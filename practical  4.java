/**Develop a Java application that calculates a person's Body Mass Index (BMI). The program should ask the user for their weight in pounds 
and height in inches. Convert these values to kilograms and meters respectively (1 pound = 0.45359237 kg, 1 inch = 0.0254 meters) and then
calculate BMI (weight in kg / (height in meters) ^2). Display the calculated BMI. */


import java.util.Scanner;

class P4{
	public static void main(String args[]) {
	Scanner sc = new Scanner(System.in);

	double pound;
	double inches;

	System.out.println("Enter your weight in pounds : ");
	pound = sc.nextDouble();

	System.out.println("Enter your height in inches : ");
	inches = sc.nextDouble();

	double kg = (pound * 0.45359237);
	double meter = (inches * 0.0254);
	
	double BMI = (kg / meter * meter);

	System.out.println("Your BMI is "+ BMI);	

	sc.close();	
	}

}